package Quiz06;

public class BreadAndChage {

	private int bread;
	private int change;
	
	public BreadAndChage(int bread,int change) {
		this.bread = bread;
		this.change = change;
	}
		
	public int getBread() {
		return bread;
	}

	public void setBread(int bread) {
		this.bread = bread;
	}

	public int getChange() {
		return change;
	}

	public void setChange(int change) {
		this.change = change;
	}

	
	
}
